package com.ezdk.code.generator.beans;

import com.fasterxml.jackson.annotation.JsonIgnoreProperties;

@JsonIgnoreProperties(ignoreUnknown = true)
public class JavaObject {
	private String columnName;
	private String dataType;
	private String variableName;
	private String variableType;
	private String tableName;
	private String Nullable;
	private String databaseName;
	private String xType;
	private String allowB;
	
	public JavaObject() {
	}
	
	public JavaObject( String databaseName ) {
		this.databaseName = databaseName;
	}
	
	public JavaObject( String variableName, String variableType) {
		this.variableName = variableName;
		this.variableType = variableType;
	}
	
	public JavaObject(String columnName, String dataType, String variableName, String variableType, String Nullable, String xType, String allowB) {
		this.columnName = columnName;
		this.dataType = dataType;
		this.variableName = variableName;
		this.variableType = variableType;
		this.Nullable = Nullable;
		this.xType = xType;
		this.allowB = allowB;
	}

	/**
	 * @return the columnName
	 */
	public String getColumnName() {
		return columnName;
	}

	/**
	 * @param columnName the columnName to set
	 */
	public void setColumnName(String columnName) {
		this.columnName = columnName;
	}

	/**
	 * @return the dataType
	 */
	public String getDataType() {
		return dataType;
	}

	/**
	 * @param dataType the dataType to set
	 */
	public void setDataType(String dataType) {
		this.dataType = dataType;
	}

	/**
	 * @return the variableName
	 */
	public String getVariableName() {
		return variableName;
	}

	/**
	 * @param variableName the variableName to set
	 */
	public void setVariableName(String variableName) {
		this.variableName = variableName;
	}

	/**
	 * @return the variableType
	 */
	public String getVariableType() {
		return variableType;
	}

	/**
	 * @param variableType the variableType to set
	 */
	public void setVariableType(String variableType) {
		this.variableType = variableType;
	}

	/**
	 * @return the tableName
	 */
	public String getTableName() {
		return tableName;
	}

	/**
	 * @param tableName the tableName to set
	 */
	public void setTableName(String tableName) {
		this.tableName = tableName;
	}
	
	/**
	 * @return the databaseName
	 */
	public String getDatabaseName() {
		return databaseName;
	}

	/**
	 * @param databaseName the databaseName to set
	 */
	public void setDatabaseName(String databaseName) {
		this.databaseName = databaseName;
	}
	
	/**
	 * @return the nullable
	 */
	public String getNullable() {
		return Nullable;
	}

	/**
	 * @param nullable the nullable to set
	 */
	public void setNullable(String nullable) {
		this.Nullable = nullable;
	}
	
	/**
	 * @return the xType
	 */
	public String getxType() {
		return xType;
	}

	/**
	 * @param xType the xType to set
	 */
	public void setxType(String xType) {
		this.xType = xType;
	}

	/**
	 * @return the allowBlank
	 */
	public String getAllowB() {
		return allowB;
	}

	/**
	 * @param allowBlank the allowBlank to set
	 */
	public void setAllowB(String allowB) {
		this.allowB = allowB;
	}
}
